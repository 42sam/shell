#include "../libft.h"

char	*tab_to_space(char *s)
{
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (ft_isspace(s[i]))
			s[i] = ' ';
		i++;
	}
	return (s);
}
