#include "../libft.h"

int		is_digitstr(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (!((s[i] >= '0') && (s[i] <= '9')))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
